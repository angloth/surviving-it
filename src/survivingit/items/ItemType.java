package survivingit.items;

/**
 * Enum class for item types.
 * Works as a more readable version of Item ID.
 */
public enum ItemType {
    /**
     * None item type
     */
    NONE,
    /**
     * Knife item type
     */
    KNIFE,
    /**
     * Berry item type
     */
    BERRY,
    /**
     * Boots item type
     */
    BOOTS,
    /**
     * Flint and steel item type
     */
    FLINT_AND_STEEL
}
